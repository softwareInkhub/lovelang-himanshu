// Helper function to get optimized image URL with custom width
export function getOptimizedImageUrl(baseUrl: string, width: number = 800): string {
  // Remove existing width parameter and add new one
  const url = new URL(baseUrl);
  url.searchParams.set('width', width.toString());
  return url.toString();
}

